namespace detail { namespace
{
	struct ConstCharLessThan
	{
		bool operator()(const char* left, const char* right) const
		{
			return std::strcmp(left, right) < 0;
		}
	};
	using GlyphMapping = std::map<const char*, QChar, ConstCharLessThan>;
	extern GlyphMapping MATERIAL_GLYPHS;

	const char* MATERIAL_FONT_FAMILY = "Material Design Icons";
	const char* MATERIAL_FONT_TTF = ":/materialdesignicons-webfont.ttf";
} }

QPixmap material::pixmap(const QString& name, const QSize& size, const QColor& color)
{
	static std::once_flag once_;
	std::call_once(once_, []{ QFontDatabase::addApplicationFont(detail::MATERIAL_FONT_TTF); });

	QPixmap pix(size);
	pix.fill(Qt::transparent);

	QPainter painter(&pix);

	painter.setBrush(Qt::transparent);
	painter.setPen(color);
	/// So far the code below uses magic 60% of the pixel size requested to get the point size to use
	/// Think of something smarter than that
	const int font_point_size = std::min(size.width(), size.height()) * 3/5;
	painter.setFont(QFont(detail::MATERIAL_FONT_FAMILY, font_point_size));
	painter.drawText(pix.rect(), Qt::AlignCenter, detail::MATERIAL_GLYPHS[name.toLocal8Bit()]);

	return pix;

}

